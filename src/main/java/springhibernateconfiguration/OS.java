package springhibernateconfiguration;

public class OS {
	int os_id;
	String os_name;

	public int getOs_id() {
		return os_id;
	}

	public void setOs_id(int os_id) {
		this.os_id = os_id;
	}

	public String getOs_name() {
		return os_name;
	}

	public void setOs_name(String os_name) {
		this.os_name = os_name;
	}

}
